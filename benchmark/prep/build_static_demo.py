#!/usr/bin/env python3
"""Build a self-contained STATIC demo of the training-similarity viewer.

The live viewer (``app/``) is served from a web root and references every file
with a leading ``/`` (``/systems.json``, ``/app.js``, ``/systems/<id>/...``).
The per-system PDB/CIF data (~188 MB across 94 dirs, 47 in the manifest) is
NOT shipped with the app — it is regenerated by the ``prep/`` pipeline.

This script bakes a subset of that data into a standalone ``demo/`` folder that
can be served from its OWN directory as web root (paths are rewritten to be
relative, ``./`` instead of ``/``), so:

    cd demo && python3 -m http.server 8000   # then open http://localhost:8000

NOTE: like any Mol* app, the demo loads structures via ``fetch`` and therefore
needs a static HTTP server; opening ``demo/index.html`` directly over
``file://`` will fail (browser CORS blocks ``fetch`` of local files).

Selection rule
--------------
Keep only systems that HAVE a most-similar pre-cutoff training complex, i.e.
``train_pdb`` is not null. Those are the systems the training-similarity story
is about. The demo is size-budgeted to <= 200 MB; if the kept set exceeds the
budget it is round-robin subsampled and the dropped systems are logged.

Group (within-target overlay) mode is intentionally omitted from the demo —
the demo focuses on the single-system training-similarity view.
"""
import json
import os
import re
import shutil
import sys

HERE = os.path.dirname(os.path.abspath(__file__))
# prep/ lives next to app/ and demo/ inside the package.
PKG = os.path.dirname(HERE)
APP = os.path.join(PKG, "app")
DEMO = os.path.join(PKG, "demo")
# Per-system data lives in the ORIGINAL viewer dir, not in the package.
# Override with $VIEWER_ROOT if you relocate it.
VIEWER_ROOT = os.environ.get(
    "VIEWER_ROOT",
    os.path.abspath(os.path.join(PKG, "..", "viewer")),
)

BUDGET_BYTES = 200 * 1000 * 1000  # 200 MB

# Fields on a system / pose that point at a data file (relative to VIEWER_ROOT).
SYS_FILE_FIELDS = [
    "xtal_file", "xtal_1copy_file",
    "train_ligand_file", "train_protein_file", "cloud_file",
]
POSE_FILE_FIELDS = ["pose_file", "protein_file", "pose_disp_file", "protein_disp_file"]


def system_files(s):
    fs = []
    for f in SYS_FILE_FIELDS:
        if s.get(f):
            fs.append(s[f])
    for p in s.get("poses", []):
        for f in POSE_FILE_FIELDS:
            if p.get(f):
                fs.append(p[f])
    # de-dupe, preserve order
    seen = set()
    out = []
    for x in fs:
        if x not in seen:
            seen.add(x)
            out.append(x)
    return out


def files_size(files):
    return sum(
        os.path.getsize(os.path.join(VIEWER_ROOT, f))
        for f in files
        if os.path.exists(os.path.join(VIEWER_ROOT, f))
    )


def main():
    manifest = os.path.join(APP, "systems.json")
    data = json.load(open(manifest))
    systems = data["systems"]

    # keep = has a training neighbour AND passes the quiz's >=15 heavy-atom drug-like floor
    HEAVY_MIN = 15
    kept = [s for s in systems if s.get("train_pdb") and (s.get("n_atoms") or 0) >= HEAVY_MIN]
    dropped_no_train = [s["id"] for s in systems if not s.get("train_pdb")]
    dropped_small = [s["id"] for s in systems if s.get("train_pdb") and (s.get("n_atoms") or 0) < HEAVY_MIN]
    print(f"total systems in manifest : {len(systems)}")
    print(f"have train neighbour       : {sum(1 for s in systems if s.get('train_pdb'))}")
    print(f"no train neighbour (drop)  : {len(dropped_no_train)} -> {dropped_no_train}")
    print(f"< {HEAVY_MIN} heavy (drop)        : {len(dropped_small)} -> {dropped_small}")
    print(f"KEPT (>= {HEAVY_MIN} heavy)        : {len(kept)}")

    # Size-budget: greedily include smallest-first; round-robin drop the rest.
    sized = sorted(kept, key=lambda s: files_size(system_files(s)))
    budget_kept, budget_dropped, running = [], [], 0
    for s in sized:
        sz = files_size(system_files(s))
        if running + sz <= BUDGET_BYTES:
            budget_kept.append(s)
            running += sz
        else:
            budget_dropped.append(s["id"])
    print(
        f"budget kept                : {len(budget_kept)} "
        f"({running/1e6:.1f} MB / {BUDGET_BYTES/1e6:.0f} MB)"
    )
    if budget_dropped:
        print(f"budget dropped (>200 MB)   : {budget_dropped}")
    else:
        print("budget dropped (>200 MB)   : none (all with-train systems fit)")

    # Rebuild demo/ from scratch.
    if os.path.exists(DEMO):
        shutil.rmtree(DEMO)
    os.makedirs(DEMO)

    # 1) copy per-system data for the kept systems.
    n_files = 0
    for s in budget_kept:
        for f in system_files(s):
            src = os.path.join(VIEWER_ROOT, f)
            dst = os.path.join(DEMO, f)
            os.makedirs(os.path.dirname(dst), exist_ok=True)
            shutil.copy2(src, dst)
            n_files += 1
    print(f"copied per-system files    : {n_files}")

    # 2) write the trimmed manifest.
    demo_manifest = {"systems": budget_kept}
    with open(os.path.join(DEMO, "systems.json"), "w") as fh:
        json.dump(demo_manifest, fh)

    # 3) copy + rewrite the front-end so it is served from demo/ as web root
    #    (absolute "/" -> relative "./"). Group mode is stripped from the demo.
    rewrite_frontend()

    print(f"\ndemo written to            : {DEMO}")
    print("serve with                 : cd demo && python3 -m http.server 8000")


def rewrite_frontend():
    # index.html: relative script src, drop group.js include + the group tab.
    html = open(os.path.join(APP, "index.html")).read()
    html = html.replace('src="/app.js"', 'src="./app.js"')
    # remove the group.js include entirely (demo has no group mode)
    html = re.sub(r'\s*<script src="/group\.js"></script>', "", html)
    # drop the "Within-target overlay" tab (no group data in the demo)
    html = re.sub(
        r'\s*<div class="tab" id="tab-group" data-mode="group">Within-target overlay</div>',
        "", html,
    )
    with open(os.path.join(DEMO, "index.html"), "w") as fh:
        fh.write(html)

    # app.js: rewrite every absolute data URL to relative.
    js = open(os.path.join(APP, "app.js")).read()
    js = js.replace("fetch('/systems.json", "fetch('./systems.json")
    # loadStruct('/' + x) -> loadStruct('./' + x)
    js = js.replace("loadStruct('/' +", "loadStruct('./' +")
    # group.js is not loaded in the demo; app.js only *references* window.GROUP
    # inside switchMode, guarded by the group tab which we leave inert. To be
    # safe, no-op the group entry so a stray click can't throw.
    js = js.replace(
        "await window.GROUP.enter();",
        "if (window.GROUP) await window.GROUP.enter();",
    )
    with open(os.path.join(DEMO, "app.js"), "w") as fh:
        fh.write(js)


if __name__ == "__main__":
    sys.exit(main())
